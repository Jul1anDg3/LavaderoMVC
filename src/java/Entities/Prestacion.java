package Entities;

public class Prestacion {
    int id;
    String nombre;
    String descripcion;
    String funcionario;
    int precio;

    public Prestacion() {
    }

    public Prestacion(int id, String nombre, String descripcion, String funcionario, int precio) {
        this.id = id;
        this.nombre = nombre;
        this.descripcion = descripcion;
        this.funcionario = funcionario;
        this.precio = precio;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getFuncionario() {
        return funcionario;
    }

    public void setFuncionario(String funcionario) {
        this.funcionario = funcionario;
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }
    
    
}
